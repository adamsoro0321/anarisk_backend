            if not ind:
                # Copie pour ne pas modifier la liste source
                row = {k: v for k, v in data.items() if v != ""}
                # actif est un booléen non-nullable : normaliser si présent, sinon laisser le défaut du modèle
                if "actif" in row:
                    row["actif"] = str(row["actif"]).lower() in ("1", "true", "oui", "yes")
                # date_creation et date_modification ont des defaults côté modèle → les supprimer
                row.pop("date_creation", None)
                row.pop("date_modification", None)

                # 'type' est un mot-clé Python/SQLAlchemy : le passer via setattr
                type_value = row.pop("type", None)
                ind = Indicateur(**row)
                if type_value:
                    setattr(ind, "type", type_value)
                db.session.add(ind)
                indicateurs_inserted += 1
